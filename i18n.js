let currentLanguage = localStorage.getItem('appLanguage') || 'pt';

function t(key) {
  return translations[currentLanguage][key] || translations['pt'][key];
}

function updateTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (element.tagName === 'INPUT') {
      if (element.getAttribute('type') === 'text' || element.getAttribute('type') === 'number') {
        element.placeholder = t(key + '-placeholder') || element.placeholder;
      }
    } else if (element.tagName === 'OPTION') {
      // Skip option translations
    } else {
      element.textContent = t(key);
    }
  });
  
  // Update button theme text
  const themeBtn = document.getElementById('theme-toggle');
  const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
  themeBtn.textContent = isDark ? t('light-mode') : t('dark-mode');
}

document.addEventListener('DOMContentLoaded', () => {
  const languageSelect = document.getElementById('language-select');
  const themeToggle = document.getElementById('theme-toggle');
  
  // Load saved theme
  const savedTheme = localStorage.getItem('appTheme') || 'light';
  document.documentElement.setAttribute('data-theme', savedTheme);
  
  // Set language selector
  languageSelect.value = currentLanguage;
  updateTranslations();
  
  // Language change listener
  languageSelect.addEventListener('change', (e) => {
    currentLanguage = e.target.value;
    localStorage.setItem('appLanguage', currentLanguage);
    updateTranslations();
  });
  
  // Theme toggle listener
  themeToggle.addEventListener('click', () => {
    const newTheme = document.documentElement.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    document.documentElement.setAttribute('data-theme', newTheme);
    localStorage.setItem('appTheme', newTheme);
    updateTranslations();
  });
});
